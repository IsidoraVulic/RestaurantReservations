    private User login() throws Exception {

        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());

        return Controller.getInstace().login(username, password);
    }

    private void loginUser(User user) {
        if (user != null) {
            System.out.println(user.toString());
            MainForm form = new MainForm();
            form.setLoggedUser(user);
            form.setVisible(true);
            dispose();
            JOptionPane.showMessageDialog(form, "Korisnik je uspešno ulogovan");
        } else {

            JOptionPane.showMessageDialog(this, "Korisnik nije uneo tačne podatke", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void connect() throws IOException {
        Controller.getInstace().connect();
    }

